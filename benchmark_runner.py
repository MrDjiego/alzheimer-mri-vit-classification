import tensorflow as tf
from tensorflow.keras import optimizers, callbacks
from tensorflow.keras.mixed_precision import set_global_policy
import matplotlib.pyplot as plt
import seaborn as sns
import numpy as np
import time
from sklearn.metrics import confusion_matrix

# --- IMPORTACIÓN DE TUS PROPIOS MÓDULOS ---
from dataset_builder import prepare_data
from data_pipeline import create_datasets
from models.hybrid_vit import build_experiment_model

# --- 1. CONFIGURACIÓN GLOBAL ---
set_global_policy('mixed_float16')
EPOCHS_PHASE_1 = 6
EPOCHS_PHASE_2 = 12
diagnosis_mapping = {0: 'MildDemented', 1: 'ModerateDemented', 2: 'NonDemented', 3: 'VeryMildDemented'}

print(f"TensorFlow Version: {tf.__version__}")

# --- 2. PREPARACIÓN DE DATOS ---
print(">>> Descargando y preparando dataset...")
train_paths, val_paths, train_labels, val_labels = prepare_data()
train_ds, val_ds = create_datasets(train_paths, val_paths, train_labels, val_labels)

# --- 3. DEFINICIÓN DE ESCENARIOS ---
vit_config_modified = {
    'embed_dim': 256,
    'num_heads': 8,
    'mlp_dim': 1024,
    'layers': 4
}

scenarios = [
    {"name": "1. Baseline (ViT Default)", "use_vit": True, "use_aug": False, "vit_cfg": None},
    {"name": "2. Augmented (ViT + Aug)", "use_vit": True, "use_aug": True, "vit_cfg": None},
    {"name": "3. ViT Modificado (Heavier)", "use_vit": True, "use_aug": False, "vit_cfg": vit_config_modified},
    {"name": "4. Solo CNN (Sin ViT)", "use_vit": False, "use_aug": False, "vit_cfg": None}
]

results = {"history": {}, "params": {}, "time": {}, "final_acc": {}}
confusion_matrices = {}

# --- 4. BUCLE DE EJECUCIÓN ---
for sc in scenarios:
    print(f"\n{'#'*60}")
    print(f"EJECUTANDO ESCENARIO: {sc['name']}")
    print(f"{'#'*60}")

    tf.keras.backend.clear_session()

    # Construir usando la función de tu librería
    model, base_model, freeze_point = build_experiment_model(
        use_vit=sc['use_vit'],
        use_augmentation=sc['use_aug'],
        vit_config=sc['vit_cfg']
    )

    total_params = model.count_params()
    results["params"][sc['name']] = total_params
    print(f"> Total Parámetros: {total_params:,}")

    start_time = time.time()

    # Fase 1
    print("--- Fase 1: Entrenamiento de Cabeza ---")
    model.compile(optimizer=optimizers.Adam(1e-4), loss="categorical_crossentropy", metrics=["accuracy"])
    model.fit(train_ds, validation_data=val_ds, epochs=EPOCHS_PHASE_1, verbose=1)

    # Fase 2 (Fine-Tuning)
    print("--- Fase 2: Fine-Tuning Backbone ---")
    for layer in base_model.layers[freeze_point:]:
        layer.trainable = True

    model.compile(optimizer=optimizers.Adam(1e-5), loss="categorical_crossentropy", metrics=["accuracy"])
    early_stop = callbacks.EarlyStopping(monitor="val_accuracy", patience=4, restore_best_weights=True)

    hist = model.fit(train_ds, validation_data=val_ds, epochs=EPOCHS_PHASE_2, callbacks=[early_stop], verbose=1)

    end_time = time.time()
    elapsed_time = end_time - start_time

    # Guardar métricas
    results["history"][sc['name']] = hist.history
    results["time"][sc['name']] = elapsed_time
    results["final_acc"][sc['name']] = max(hist.history['val_accuracy'])

    print(f"> Tiempo Total: {elapsed_time:.2f} s")
    print(f"> Accuracy Final (Val): {results['final_acc'][sc['name']]:.4f}")

    # Matriz de Confusión
    preds = model.predict(val_ds, verbose=0)
    y_pred = np.argmax(preds, axis=1)
    y_true = np.concatenate([y for x, y in val_ds], axis=0)
    y_true = np.argmax(y_true, axis=1)
    confusion_matrices[sc['name']] = confusion_matrix(y_true, y_pred)

print("\n>>> ¡BENCHMARK COMPLETADO! GENERANDO REPORTE... <<<")

# --- 5. VISUALIZACIÓN Y REPORTES ---
print("\n" + "="*80)
print(f"{'MODELO':<30} | {'PARAMS':<12} | {'TIEMPO (s)':<10} | {'VAL ACC':<10}")
print("="*80)
for name in scenarios:
    n = name['name']
    print(f"{n:<30} | {results['params'][n]:<12,} | {results['time'][n]:<10.1f} | {results['final_acc'][n]:.4f}")
print("="*80 + "\n")

plt.figure(figsize=(14, 6))
plt.subplot(1, 2, 1)
colors = ['r', 'g', 'purple', 'b']
for i, sc in enumerate(scenarios):
    name = sc['name']
    plt.plot(results["history"][name]['val_accuracy'], label=name, color=colors[i], linewidth=2)
plt.title('Comparativa: Validation Accuracy')
plt.xlabel('Épocas (Fine-Tuning)')
plt.ylabel('Accuracy')
plt.legend()
plt.grid(True, alpha=0.3)

plt.subplot(1, 2, 2)
for i, sc in enumerate(scenarios):
    name = sc['name']
    plt.plot(results["history"][name]['val_loss'], label=name, color=colors[i], linewidth=2, linestyle='--')
plt.title('Comparativa: Validation Loss')
plt.xlabel('Épocas (Fine-Tuning)')
plt.ylabel('Loss')
plt.legend()
plt.grid(True, alpha=0.3)

plt.tight_layout()
plt.show()

plt.figure(figsize=(20, 5))
for i, sc in enumerate(scenarios):
    name = sc['name']
    plt.subplot(1, 4, i+1)
    sns.heatmap(confusion_matrices[name], annot=True, fmt='d', cmap='Blues',
                xticklabels=diagnosis_mapping.values(),
                yticklabels=diagnosis_mapping.values(), cbar=False)
    plt.title(name, fontsize=10)
    plt.xlabel('Predicción')
    if i == 0: plt.ylabel('Real')
plt.tight_layout()
plt.show()